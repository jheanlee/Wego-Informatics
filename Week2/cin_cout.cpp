#include <iostream>
#include <string>
using namespace std;

int main() {
/*--------程式開始--------*/

/*--------變數宣告--------*/
  int a;
  string b;
  float c;

/*--------輸入--------*/
  cin >> a >> b >> c; //cin會依序把資料存入 a, b, c 中

/*--------輸出--------*/
  cout << b << a << c << endl; //cout會依序輸出 b, a, c 的資料到畫面上
  cout << "hi" << '\n' << "bye"; // endl = '\n' = 換行

/*--------程式結束--------*/
  return 0;
}



