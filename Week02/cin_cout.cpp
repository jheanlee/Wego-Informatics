#include <bits/stdc++.h>
using namespace std;

int main() {
/*--------程式開始--------*/

/*--------變數宣告--------*/
  int a;
  string b; // 文字
  float c; // 小數

/*--------輸入--------*/
  cin >> a >> b >> c; //cin會依序把資料存入 a, b, c 中

/*--------輸出--------*/
  cout << b << " " << a << c << endl; //cout會依序輸出 b, a, c 的資料到畫面上
  cout << "hi" << '\n' << "bye"; // endl = '\n' = 換行

  /*
   * 1 abc 1.2
   * bac -> abc 11.2
   * hi
   * bye
   */

/*--------程式結束--------*/
  return 0;
}



